export default (c) => {
    if(c === 'emf') return 'EMF Level 5'
    else if(c === 'dots') return 'D.O.T.S Projector'
    else if(c === 'box') return 'Spirit Box'
    else if(c === 'orb') return 'Ghost Orb'
    else if(c === 'ultra') return 'Ultraviolet'
    else if(c === 'freezing') return 'Freezing Temps'
}