export default (e) => {
    if(e === 'emf') return 'EMF Level 5'
    else if(e === 'ultra') return 'Ultraviolet'
    else if(e === 'writing') return 'Ghost Writing'
    else if(e === 'freezing') return 'Freezing Temperatures'
    else if(e === 'dots') return 'D.O.T.S Projector'
    else if(e === 'orb') return 'Ghost Orb'
    else if(e === 'box') return 'Spirit Box'
    else return ''
}